        Matematika (dari bahasa Yunani Kuno μάθημα (máthēma), berarti "pengetahuan, pemikiran, pengkajian, pembelajaran"), adalah bidang ilmu, yang mencakup studi tentang topik-topik seperti bilangan (aritmetika dan teori bilangan), rumus dan struktur terkait (aljabar), bangun dan ruang tempat mereka berada (geometri), dan besaran serta perubahannya (kalkulus dan analisis) Tidak ada kesepakatan umum tentang ruang lingkup yang tepat atau status epistemologisnya.

Matematika selalu berkembang, misalnya di Tiongkok pada tahun 300 SM, di India pada tahun 100 M, dan di Arab pada tahun 800 M, hingga zaman Renaisans, ketika temuan baru matematika berinteraksi dengan penemuan ilmiah baru yang mengarah pada peningkatan yang cepat di dalam laju penemuan matematika. Berlanjut hingga kini, matematika digunakan di seluruh dunia sebagai alat penting di berbagai bidang, termasuk ilmu alam, teknik, kedokteran/medis, dan ilmu sosial seperti ekonomi, dan psikologi. Matematika terapan, cabang matematika yang melingkupi penerapan pengetahuan matematika ke bidang-bidang lain, mengilhami dan membuat penggunaan temuan-temuan matematika baru, dan kadang-kadang mengarah pada pengembangan disiplin-disiplin ilmu yang sepenuhnya baru, seperti statistika dan teori permainan.

Matematika banyak digunakan dalam ilmu pengetahuan untuk fenomena pemodelan. Hal ini memungkinkan ekstraksi perkiraan kuantitatif dari hukum-hukum percobaan. Misalnya, pergerakan planet dapat diprediksi dengan akurasi tinggi menggunakan hukum gravitasi Newton yang dipadukan dengan perhitungan matematis. Ketakbergantungan kebenaran matematis dari percobaan manapun menyiratkan bahwa keakuratan perkiraan semacam itu hanya bergantung pada kecukupan model untuk menggambarkan kenyataan. Jadi, ketika munculnya beberapa perkiraan yang tidak tepat, itu berarti bahwa model harus diperbaiki atau diubah, bukan berarti matematika salah. Misalnya, presesi apsis atau perihelium Merkurius tidak dapat dijelaskan dengan hukum gravitasi Newton, tetapi dijelaskan secara akurat oleh relativitas umum Einstein. Pengesahan percobaan teori Einstein ini menunjukkan bahwa hukum gravitasi Newton hanyalah hampiran (yang masih sangat akurat dalam kehidupan sehari-hari).

Matematika sangat penting di banyak bidang, termasuk ilmu alam, rekayasa, kedokteran, keuangan, ilmu komputer, dan ilmu sosial. Beberapa bidang matematika, seperti statistika dan teori permainan, dikembangkan dalam korelasi langsung dengan terapannya, dan sering dikelompokkan dengan nama matematika terapan. Bidang matematika lainnya dikembangkan secara independen dari aplikasi apapun (dan oleh karena itu disebut matematika murni), tetapi aplikasi praktis sering ditemukan kemudian. Contoh yang tepat adalah masalah faktorisasi prima, yang merujuk kepada Euklides, tetapi yang tidak memiliki aplikasi praktis sebelum digunakan dalam sistemkripto RSA (untuk keamanan jaringan komputer).


